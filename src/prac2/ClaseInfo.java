package prac2;

public class ClaseInfo {
    private int sum = 0;
    private int dif = 0;


    
    public ClaseInfo(int sum, int dif) {
        this.sum = sum;
        this.dif = dif;
    }
    public int getSum() {
        return sum;
    }
    public void setSum(int sum) {
        this.sum = sum;
    }
    public int getDif() {
        return dif;
    }
    public void setDif(int dif) {
        this.dif = dif;
    }

    
    
}
